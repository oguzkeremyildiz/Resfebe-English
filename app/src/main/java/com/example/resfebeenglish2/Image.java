package com.example.resfebeenglish2;

public class Image {
    private String fileNo;
    private String word;

    public Image(String fileNo, String word){
        this.fileNo = fileNo;
        this.word = word;
    }

    public String getFileNo() {
        return fileNo;
    }

    public String getWord() {
        return word;
    }


}
